#[inline(always)]
fn lookup(x: u32) -> u32 {
    u32::from_ne_bytes(x.to_ne_bytes().map(|x| LOOKUP_TABLE[x as usize]))
}

pub fn expand_40(key: u64) -> [u32; 16] {
    // key ~ 01234567; left ~ 6420; right ~ 7531;
    let key = (key & 0x00ffff0000ffff00)
        | (key & 0xff000000ff000000) >> 24
        | (key & 0x000000ff000000ff) << 24;
    let key = (key & 0x0000ffffffff0000)
        | (key & 0xffff000000000000) >> 48
        | (key & 0x000000000000ffff) << 48;

    let mut left: u32 = (key >> 32) as u32;
    let mut right: u32 = (key & 0xffffffff) as u32;

    let mut k_expanded: [u32; 16] = [0; 16];
    for i in 0..16 {
        let s: u32 = lookup(right);
        let s = (0x00ffff00 & (s ^ left))
            | (0xff0000ff & u32::wrapping_add(s & 0xff0000ff, left & 0xff0000ff));
        left = right;
        right = (s >> 8) | (s << 24);
        k_expanded[i] = s;
    }
    return k_expanded;
}

pub fn round_function(x: u32, k: u32) -> u32 {
    lookup(x ^ k)
}

pub fn feistel(x: u64, left_key: u32, right_key: u32) -> u64 {
    let mut left: u32 = (x >> 32) as u32;
    let mut right = x as u32;

    right ^= round_function(left, right_key as u32);
    left ^= round_function(right, left_key as u32);

    ((left as u64) << 32) | (right as u64)
}

#[test]
fn test_round_function() {
    let x = 0x12345678;
    let k = 0x87654321;
    let y = round_function(x, k);
    assert_eq!(y, 0xa9c95ebb);
}
